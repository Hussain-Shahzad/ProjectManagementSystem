    public void loadList()
    {
        jList1.setModel(dm);
        int size=grp.getStds().size();
        for (int i=0;i<size;i++)
        {
            dm.addElement(grp.getStds().get(i).getName());
        }
    }
    public void clearList()
    {
        dm.clear();
        jList1.setModel(dm);
    }

